import { createRequire } from "node:module";
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

import { format, resolveConfig } from "prettier";

const require = createRequire(import.meta.url);

const PACKAGE_MAP = {
  main: "@ui5/webcomponents",
  fiori: "@ui5/webcomponents-fiori",
  compatibility: "@ui5/webcomponents-compat",
  ai: "@ui5/webcomponents-ai",
};

const toModuleSpecifiers = (packageName, manifest) => {
  const componentModules = manifest.modules
    .filter((moduleEntry) =>
      (moduleEntry.declarations ?? []).some(
        (declaration) => declaration.customElement,
      ),
    )
    .map((moduleEntry) => `${packageName}/${moduleEntry.path}`);

  return [
    `${packageName}/dist/Assets.js`,
    ...Array.from(new Set(componentModules)).sort(),
  ];
};

const getManifestPath = (packageName) =>
  require.resolve(`${packageName}/dist/custom-elements.json`);

const formatManifestFile = (entries) => {
  const body = Object.entries(entries)
    .map(([key, imports]) => {
      const lines = imports
        .map((item) => `    () => import(${JSON.stringify(item)}),`)
        .join("\n");
      return `  ${JSON.stringify(key)}: [\n${lines}\n  ],`;
    })
    .join("\n");

  return (
    `// Generated by scripts/generate-ui5-package-manifests.mjs.\n` +
    `// Commit this file so runtime package loading stays deterministic.\n\n` +
    `export const ui5PackageLoaders = {\n${body}\n} as const;\n`
  );
};

const entries = {};

for (const [packageKey, packageName] of Object.entries(PACKAGE_MAP)) {
  const manifestPath = getManifestPath(packageName);
  const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
  entries[packageKey] = toModuleSpecifiers(packageName, manifest);
}

const outputFileUrl = new URL(
  "../src/generated/ui5-package-manifests.ts",
  import.meta.url,
);
const outputFilePath = fileURLToPath(outputFileUrl);
const prettierConfig = (await resolveConfig(outputFilePath)) ?? {};
const formattedContents = await format(formatManifestFile(entries), {
  ...prettierConfig,
  filepath: outputFilePath,
});

await writeFile(outputFileUrl, formattedContents, "utf8");
